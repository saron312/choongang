package ssss;

public class Emp {
     int employee_id;
     String first_name;
     int salary;
     Emp(int employee_id, String first_name) {
    	 this.employee_id = employee_id;
    	 this.first_name = first_name;
     }
     Emp(int employee_id, String first_name, int salary) {
    	 this.employee_id = employee_id;
    	 this.first_name = first_name;
    	 this.salary = salary;
     }
}
