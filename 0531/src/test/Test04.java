package test;

public class Test04 {
	public static void main(String[] args) {
		System.out.println(3%2);
		System.out.println(5%4);
		
		System.out.println(3%2==1 || 5%3==1);
		System.out.println(3%2==1 | 5%3==1);
		System.out.println(3%2==1 && 5%4==1);
		System.out.println(3%2==1 & 5%4==1);
		
		System.out.println(3%2==1 ^ 5%4==1);
		System.out.println(3%2==1 ^ 5%3==1);
		
	}
}
